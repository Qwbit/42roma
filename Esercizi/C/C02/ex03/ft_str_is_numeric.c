#include    <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 1;
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
		{
			n = 0;
		}
		i++;
	}
	return (n);
}
