#include <unistd.h>
#include <stdio.h>

void	ft_putstr(char *str)
{
	int		i;
	char	a;

	a = '0';
	while (a != '\0')
	{
		a = *(str + i);
		write(1, &a, 1);
		i++;
	}
}
